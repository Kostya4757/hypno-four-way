function onEvent(name,value1,value2)	
if name == 'Play Animation' then 
	if value1 == 'White' then
		setProperty('White.visible', true);
		setProperty('Lullaby.visible', true);
	end

	if value1 == 'Brim' then
		setProperty('Jojo.visible', false);
		setProperty('Brim.visible', true);
	end	

	if value1 == 'Hypno' then
		setProperty('Brim.visible', false);
		setProperty('Hypno.visible', true);		
	end	

	if value1 == 'Hell' then
		setProperty('Hypno.visible', false);		
		setProperty('Hell.visible', true);	
	end		

	if value1 == 'Moni' then
		setProperty('Hell.visible', false);		
		setProperty('Moni.visible', true);	
		setProperty('Black.visible', true);
		setProperty('Barrer1.visible', true);
		setProperty('Barrer2.visible', true);
	end

	if value1 == 'Shitno' then
		setProperty('Moni.visible', false);	
		setProperty('Black.visible', false);
		setProperty('Barrer1.visible', false);
		setProperty('Barrer2.visible', false);
		setProperty('Shitno1.visible', true);
		setProperty('Shitno2.visible', true);
	end

	if value1 == 'Bendy' then
		setProperty('Shitno1.visible', false);
		setProperty('Shitno2.visible', false);
		setProperty('Bendy.visible', true);
	end

	if value1 == 'Cuphead' then
		setProperty('Bendy.visible', false);
		setProperty('Cuphead.visible', true);
	end

	if value1 == 'Monika' then
		setProperty('Cuphead.visible', false);
		setProperty('Monika.visible', true);
	end

	if value1 == 'Sayori' then
		setProperty('Monika.visible', false);
		setProperty('Sayori.visible', true);
		setProperty('Poem.visible', true);

	end

	if value1 == 'Natsuki' then
		setProperty('Sayori.visible', false);
		setProperty('Poem.visible', false);
		setProperty('Natsuki.visible', true);
		setProperty('Static.visible', true);
	end

	if value1 == 'Yuri' then
		setProperty('Natsuki.visible', false);
		setProperty('Static.visible', false);
		setProperty('Yuri.visible', true);
	end

	if value1 == 'Cold' then
		setProperty('Yuri.visible', false);
		setProperty('Cold.visible', true);
	end

	if value1 == 'Final' then
		setProperty('Cold.visible', false);
		setProperty('Sans.visible', true);
		setProperty('nat.visible', true);
		setProperty('Static2.visible', true);
		setProperty('Barrer3.visible', true);
	end

	if value1 == 'Final2' then
		setProperty('Sans.visible', false);
		setProperty('nat.visible', false);
		setProperty('Static2.visible', false);
		setProperty('Cup.visible', true);
		setProperty('Sayo.visible', true);
		setProperty('Poem2.visible', true);
	end
	
	if value1 == 'Final3' then
		setProperty('Barrer3.visible', false);
		setProperty('Cup.visible', false);
		setProperty('Sayo.visible', false);
		setProperty('Poem2.visible', false);
		setProperty('Ben.visible', true);
		setProperty('BendyNight.visible', true);
	end

	if value1 == 'Marcos' then
		setProperty('Ben.visible', false);
		setProperty('BendyNight.visible', false);
		setProperty('Marcos.visible', true);
	end
end
